from datasets import Dataset
from ragas import evaluate
from ragas.metrics import faithfulness, answer_relevancy, context_precision, context_recall

# 🟢 แก้ไขจุดที่ 3: อัปเดตการ Import ChatOllama ให้เป็นเวอร์ชันใหม่ล่าสุด
from langchain_ollama import ChatOllama
from langchain_huggingface import HuggingFaceEmbeddings
from ragas.run_config import RunConfig

# โหลดฟังก์ชันถาม-ตอบบอทของคุณมาใช้
from rag.rag_pipeline import ask_rag 

# ==========================================
# ⚙️ 1. ตั้งค่าระบบ
# ==========================================
local_judge = ChatOllama(model="qwen2.5:7b", temperature=0.0)
local_embeddings = HuggingFaceEmbeddings(model_name="BAAI/bge-m3")

# 🔴 สำคัญ: ใส่ ID ของบอท HR ที่คุณต้องการทดสอบ
TEST_BOT_ID = "bot_ff592db3" 

# ==========================================
# 📝 2. ชุดข้อสอบ (Test Cases ทั้ง 11 ข้อ)
# ==========================================
questions = [
    "พนักงานจะได้สิทธิลาพักร้อนกี่วันต่อปี และมีเงื่อนไขอะไรบ้าง?",
    "ในวันศุกร์ พนักงานสามารถแต่งกายแบบไหนมาทำงานได้บ้าง?",
    "ประกันสุขภาพกลุ่มให้วงเงินค่ารักษาพยาบาลผู้ป่วยนอก (OPD) เท่าไหร่?",
    "หากพนักงานลืมลงเวลาเข้า-ออกงานในระบบ HRIS จะต้องดำเนินการแก้ไขอย่างไร?",
    "หากมาทำ OT ในวันหยุดนักขัตฤกษ์ จะได้รับค่าล่วงเวลาในอัตราเท่าไหร่ และต้องขออนุมัติล่วงหน้ากี่วัน?",
    "การลาป่วยมีเงื่อนไขการขอเอกสารเพิ่มเติมอย่างไร หากต้องลาป่วยติดต่อกัน 4 วัน?",
    "พนักงานสามารถลากิจเพื่อไปทำธุระอะไรได้บ้าง และต้องแจ้งล่วงหน้ากี่วัน?",
    "พนักงานหญิงสามารถลาคลอดได้สูงสุดกี่วัน และบริษัทจะรับผิดชอบจ่ายค่าจ้างให้กี่วัน?",
    "พนักงานสามารถขอ Work from Home ได้สูงสุดกี่วันต่อสัปดาห์ และมีเงื่อนไขการติดต่ออย่างไรในระหว่าง WFH?",
    "หากเดินทางไปทำงานต่างจังหวัด สามารถเบิกค่าที่พักได้คืนละเท่าไหร่ และต้องส่งเอกสารใบเสร็จภายในวันไหน?",
    "บทลงโทษหากพนักงานเปิดเผยความลับทางการค้าของบริษัทให้คนนอกรู้คืออะไร?"
]

ground_truths = [
    "พนักงานที่ผ่านการทดลองงานแล้ว (119 วัน) จะได้รับสิทธิลาพักร้อน 10 วันต่อปี และไม่สามารถสะสมข้ามปีได้",
    "สามารถสวมเสื้อยืดหรือกางเกงยีนส์ที่สุภาพได้ แต่งดเว้นกางเกงขาสั้นและรองเท้าแตะ",
    "วงเงิน 1,500 บาท/ครั้ง (สูงสุด 30 ครั้ง/ปี)",
    "ต้องแจ้งหัวหน้างานเพื่อขออนุมัติย้อนหลังภายใน 3 วันทำการ",
    "บริษัทจะจ่ายค่าล่วงเวลา 3 เท่าสำหรับวันหยุดนักขัตฤกษ์ โดยต้องได้รับการอนุมัติจากหัวหน้างานล่วงหน้าอย่างน้อย 1 วัน",
    "หากลาป่วยติดต่อกันเกิน 3 วันทำการ ต้องแนบใบรับรองแพทย์จากสถานพยาบาลที่ได้มาตรฐาน",
    "สามารถใช้เพื่อติดต่อราชการ งานศพ หรืองานแต่งงาน โดยต้องแจ้งล่วงหน้าอย่างน้อย 3 วันทำการ",
    "ลาคลอดได้สูงสุด 98 วัน โดยบริษัทจะจ่ายค่าจ้างให้ 45 วันแรก",
    "ขอได้สูงสุด 2 วันต่อสัปดาห์ และต้องสแตนด์บายพร้อมตอบข้อความในช่องทางสื่อสารหลักของบริษัทในช่วงเวลาทำงาน",
    "เบิกค่าที่พักได้ไม่เกิน 1,500 บาท/คืน โดยต้องส่งเอกสารใบเสร็จให้ฝ่ายบัญชีภายในวันที่ 5 ของเดือนถัดไป",
    "จะถูกดำเนินการทางวินัยขั้นสูงสุดและอาจถูกดำเนินคดีทางกฎหมาย"
]

# ==========================================
# 🤖 3. ปล่อย Scopebot เข้าห้องสอบ
# ==========================================
answers = []
contexts = []

print(f"🏃 กำลังส่งคำถาม 11 ข้อให้ Scopebot ตอบ (อาจใช้เวลา 1-2 นาทีเนื่องจากรันบน Local)...")
for i, q in enumerate(questions):
    print(f"   กำลังทำข้อ {i+1}/{len(questions)}...")
    
    result = ask_rag(q, TEST_BOT_ID, return_context=True)
    
    if isinstance(result, dict):
        answers.append(result.get("answer", "ไม่พบข้อมูล"))
        contexts.append([result.get("context", "")]) 
    else:
        answers.append(str(result))
        contexts.append([""])

# ==========================================
# 👨‍⚖️ 4. ส่งให้ qwen2.5:7b ตรวจ
# ==========================================
# 🟢 แก้ไขจุดที่ 1: ลบ [:3] ออก เพื่อให้ตรวจครบทั้ง 11 ข้อ
data = {
    "question": questions,
    "ground_truth": ground_truths,
    "answer": answers,
    "contexts": contexts
}

dataset = Dataset.from_dict(data)

# 🟢 แก้ไขจุดที่ 2: เปลี่ยนข้อความ Print ให้ถูกต้อง
print("\n⏳ Scopebot สอบเสร็จแล้ว! กำลังส่งกระดาษคำตอบให้กรรมการ qwen2.5:7b ตรวจ...")

# การเซ็ต max_workers=1 มีประโยชน์มากสำหรับการรัน Local ช่วยไม่ให้การ์ดจอทำงานหนักเกินไปจนค้าง
custom_config = RunConfig(timeout=600, max_workers=1)
evaluation_result = evaluate(
    dataset=dataset,
    metrics=[faithfulness, answer_relevancy, context_precision, context_recall],
    llm=local_judge,
    embeddings=local_embeddings,
    run_config=custom_config,
)

print("\n=======================================================")
print("🎯 ผลคะแนนเฉลี่ยรวมของระบบ (RAGAS Metrics Score):")
print("=======================================================")
print(evaluation_result)